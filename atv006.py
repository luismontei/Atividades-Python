##Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.
preco=float(input('Quanto ganha por hora? '))
horas=float(input('Quantas horas que você trabalhou ao mês? '))
salario= preco*horas

print('O preço da sua hora é {} \n O números de horas trabalhadas foi de {} \n O salário do mês foi de {}'.format(preco,horas,salario))
